SOURCES = [
    ('single-char', SINGLE_CHAR),
    ('hsk', HSK_VOCAB),
    ('cet', CET_VOCAB),
    ('emotion', EMOTION_VOCAB),
    ('academic', ACADEMIC_VOCAB),
    ('business', BUSINESS_VOCAB),
    ('daily-life', DAILY_VOCAB),
    ('tech', TECH_VOCAB),
    ('nature', NATURE_VOCAB),
    ('society', SOCIETY_VOCAB),
    ('abstract', ABSTRACT_VOCAB),
]

def main():
    # Read current file
    with open(WORKBANK_PATH, 'r', encoding='utf-8') as f:
        content = f.read()

    # Extract existing zh values to avoid duplicates
    existing = set(re.findall(r"zh:\s*'([^']*)'", content))
    print(f"Existing entries in file: ~{len(existing)} unique zh values")

    total_added = 0
    skipped = 0
    new_js_entries = []

    for group_name, entries in SOURCES:
        group_added = 0
        group_skipped = 0
        for e in entries:
            zh = e[0]
            if zh in existing:
                group_skipped += 1
                continue
            existing.add(zh)
            total_added += 1
            group_added += 1

            en = e[1]
            defn = e[2][:60]  # Truncate def to 60 chars
            level = e[3]
            cat = e[4]
            pos = e[5]
            pos_cn = e[6]

            # Build JS entry string
            js = f"  {{ zh: '{zh}', en: '{en}', def: '{defn}', level: '{level}', cat: '{cat}', pos: '{pos}', pos_cn: '{pos_cn}'"
            if len(e) > 7 and e[7]:
                example_en = e[7].replace("'", "\\'")
                example_cn = e[8].replace("'", "\\'") if len(e) > 8 else ''
                # Bold the word in example_cn if not already bolded
                if '**' not in example_cn and zh in example_cn:
                    example_cn = example_cn.replace(zh, f'**{zh}**')
                js += f", example: '{example_en}', example_cn: '{example_cn}'"
            js += " },"
            new_js_entries.append(js)

        print(f"  {group_name}: added {group_added}, skipped {group_skipped} (already existed)")

    print(f"\nTotal new entries to add: {total_added}")
    print(f"Skipped (duplicates): {skipped}")

    # Find the end of WORD_BANK array and insert before ];
    # The array ends with "];" on line 2823
    # We need to insert before the final line "];"
    # Find "];" that is the end of WORD_BANK
    insert_pos = content.rfind("];\n\n/* Build lookup Map")
    if insert_pos == -1:
        insert_pos = content.rfind("];")
        # Find the ]; that ends WORD_BANK, not WORD_BANK_MAP
        # WORD_BANK ends with "];", then there's a blank line and WORD_BANK_MAP
    
    # More reliable: find "/* Build lookup Map */" and go back to find the previous "];"
    map_pos = content.find("/* Build lookup Map */")
    if map_pos > 0:
        # Find the last "];" before map_pos
        array_end = content.rfind("];", 0, map_pos)
        if array_end > 0:
            insert_pos = array_end

    # Also look for the pattern ";]" that ends WORD_BANK
    if insert_pos is None or insert_pos < 0:
        # Try another approach: find the end of the WORD_BANK array
        # It's right before the blank line then const WORD_BANK_MAP
        insert_pos = content.rfind("];")
        
    print(f"Insert position: {insert_pos}")
    print(f"Context at insert: ...{content[insert_pos-20:insert_pos+20]}...")

    # Build the new entries block
    new_entries_block = "\n" + "\n".join(new_js_entries) + "\n"
    
    # Insert before the "];" that ends WORD_BANK
    modified = content[:insert_pos] + new_entries_block + content[insert_pos:]

    # Write back
    with open(WORKBANK_PATH, 'w', encoding='utf-8') as f:
        f.write(modified)

    # Count final
    final_count = len(re.findall(r"\{\s*zh:\s*'", modified))
    print(f"\nFinal entry count: ~{final_count}")
    print(f"Done! Written to {WORKBANK_PATH}")

if __name__ == '__main__':
    main()
