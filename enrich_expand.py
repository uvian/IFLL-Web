def main():
    # Read existing zh values to avoid duplicates
    with open(WORKBANK_PATH, 'r', encoding='utf-8') as f:
        content = f.read()
    
    existing_zh = set(re.findall(r"zh:\s*'([^']*)'", content))
    print(f"Existing entries: {len(existing_zh)}")
    
    # Generate all new entries
    all_words = generate_all_new_entries()
    
    # Filter out duplicates
    unique_words = []
    seen_zh = set(existing_zh)
    dup_count = 0
    for w in all_words:
        zh = w[0]
        if zh not in seen_zh:
            seen_zh.add(zh)
            unique_words.append(w)
        else:
            dup_count += 1
    
    print(f"Generated total: {len(all_words)}, duplicates removed: {dup_count}, unique new: {len(unique_words)}")
    
    # Generate JS entry strings
    new_js_entries = []
    example_count = 0
    for w in unique_words:
        zh = w[0]
        en = w[1]
        defn = w[2]
        level = w[3]
        cat = w[4]
        
        # Check if we have an example for this word (~60% coverage)
        has_example = zh in EXAMPLES_NEW
        if has_example:
            example_count += 1
            example_en, example_cn = EXAMPLES_NEW[zh]
        else:
            example_en = None
            example_cn = None
        
        entry_str = make_entry(zh, en, defn, level, cat, example_en, example_cn)
        new_js_entries.append(entry_str)
    
    print(f"Entries with examples: {example_count} / {len(unique_words)} ({example_count/max(len(unique_words),1)*100:.1f}%)")
    
    # Count by category
    cat_count = {}
    for w in unique_words:
        cat = w[4]
        cat_count[cat] = cat_count.get(cat, 0) + 1
    print("\nNew entries by category:")
    for c, n in sorted(cat_count.items(), key=lambda x: -x[1]):
        print(f"  {c}: {n}")
    
    # Count by level
    level_count = {}
    for w in unique_words:
        level = w[3]
        level_count[level] = level_count.get(level, 0) + 1
    print("\nNew entries by level:")
    for l, n in sorted(level_count.items(), key=lambda x: -x[1]):
        print(f"  {l}: {n}")
    
    # Find the closing of WORD_BANK array and append new entries
    # The array ends with "];" on its own line, preceding the WORD_BANK_MAP section
    array_end = content.rfind("\n];")
    if array_end == -1:
        print("ERROR: Could not find end of WORD_BANK array")
        return
    
    # The new entries go before the "];" symbol
    insertion_point = array_end + 1  # after the newline before ];
    
    # Build the new entries block
    new_entries_block = ",\n".join(new_js_entries)
    
    # Insert into content
    new_content = content[:insertion_point] + ",\n" + new_entries_block + content[insertion_point:]
    
    # Update the comment at the top about count
    total_entries = len(existing_zh) + len(unique_words)
    new_content = new_content.replace(
        "~280 carefully selected word pairs for immersive learning",
        f"~{total_entries} carefully selected word pairs for immersive learning"
    )
    
    # Write the file
    with open(WORKBANK_PATH, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    print(f"\n✅ File written: {WORKBANK_PATH}")
    print(f"📊 Total entries: {total_entries} (was {len(existing_zh)}, added {len(unique_words)})")
    
    # Validate: count entries after write
    with open(WORKBANK_PATH, 'r', encoding='utf-8') as f:
        final_content = f.read()
    final_count = len(re.findall(r"zh:\s*'([^']*)'", final_content))
    print(f"🔍 Validation: found {final_count} entries in final file")


if __name__ == '__main__':
    main()
