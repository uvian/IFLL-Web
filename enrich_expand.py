# ── POS helpers ──
def pos_from_cat(cat):
    """Return (pos, pos_cn) from category."""
    mapping = {
        'verb': ('verb', '动词'),
        'adj': ('adjective', '形容词'),
        'adverb': ('adverb', '副词'),
        'logic': ('conjunction', '连词'),
    }
    return mapping.get(cat, ('noun', '名词'))


def make_entry(zh, en, defn, level, cat, example_en=None, example_cn=None):
    """Build JS object string for one entry."""
    # Escape single quotes in strings
    zh = zh.replace("'", "\\'")
    en = en.replace("'", "\\'")
    defn = defn.replace("'", "\\'")
    
    pos, pos_cn = pos_from_cat(cat)
    
    parts = [
        f"  {{ zh: '{zh}', en: '{en}', def: '{defn}', level: '{level}', cat: '{cat}', pos: '{pos}', pos_cn: '{pos_cn}'"
    ]
    if example_en and example_cn:
        example_en_s = example_en.replace("'", "\\'")
        example_cn_s = example_cn.replace("'", "\\'")
        parts[0] += f", example: '{example_en_s}', example_cn: '{example_cn_s}'"
    
    parts[0] += " }"
    return parts[0]


